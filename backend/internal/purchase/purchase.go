type POStatus string

const (
	PODraft    POStatus = "DRAFT"
	POSent     POStatus = "SENT"
	POApproved POStatus = "APPROVED"
	POReceived POStatus = "RECEIVED"
	POClosed   POStatus = "CLOSED"
	POCancelled POStatus = "CANCELLED"
)

