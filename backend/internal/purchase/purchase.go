// ═══════════════════════════════════════════════════════════════════════
// Auto-PO Engine (INV-7.3.3)
// ═══════════════════════════════════════════════════════════════════════

type AutoPOConfig struct {
	MinStockThreshold int     // % of min_stock that triggers auto-PO (e.g., 50 = 50%)
	DefaultQuantity   int     // default order quantity
	DefaultVendorID   string  // fallback vendor
	Currency          string  // default currency
}

func DefaultAutoPOConfig() AutoPOConfig {
	return AutoPOConfig{
		MinStockThreshold: 50,
		DefaultQuantity:   10,
		DefaultVendorID:   "",
		Currency:          "USD",
	}
}

// AutoPOTrigger содержит информацию для автоматического создания PO.
type AutoPOTrigger struct {
	PartID      string  `json:"part_id"`
	PartName    string  `json:"part_name"`
	PartSKU     string  `json:"part_sku"`
	CurrentStock int    `json:"current_stock"`
	MinStock    int     `json:"min_stock"`
	SuggestedQty int    `json:"suggested_quantity"`
	UnitPrice   float64 `json:"unit_price"`
	VendorID    string  `json:"vendor_id,omitempty"`
}

// CheckLowStock проверяет, нужно ли создавать PO для запчасти.
func CheckLowStock(currentStock, minStock int, config AutoPOConfig) *AutoPOTrigger {
	if minStock <= 0 {
		return nil
	}

	threshold := int(float64(minStock) * float64(config.MinStockThreshold) / 100.0)
	if currentStock <= threshold {
		suggestedQty := config.DefaultQuantity
		if suggestedQty <= 0 {
			suggestedQty = minStock * 2
		}
		return &AutoPOTrigger{
			CurrentStock:  currentStock,
			MinStock:      minStock,
			SuggestedQty:  suggestedQty,
		}
	}
	return nil
}

// GeneratePONumber генерирует номер PO.
func GeneratePONumber(year int, seq int) string {
	return fmt.Sprintf("PO-%d-%04d", year, seq)
}

// CalculateLineTotal вычисляет общую сумму позиции.
func (item *POLineItem) CalculateLineTotal() {
	item.TotalPrice = float64(item.Quantity) * item.UnitPrice
}

// CalculateTotals пересчитывает суммы PO.
func (po *PurchaseOrder) CalculateTotals() {
	po.Subtotal = 0
	for i := range po.LineItems {
		po.LineItems[i].CalculateLineTotal()
		po.Subtotal += po.LineItems[i].TotalPrice
	}
	po.Total = po.Subtotal + po.Tax
}

// Summary возвращает краткую сводку по PO.
func (po *PurchaseOrder) Summary() string {
	return fmt.Sprintf("%s [%s] %s: $%.2f (%d items)",
		po.PONumber, po.Status, po.VendorName, po.Total, len(po.LineItems))
}
